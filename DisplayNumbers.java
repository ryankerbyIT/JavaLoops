/**
 * Here's a Java program that uses a for loop to display the 
 * numbers from 0 to 1000 in increments of 10:
 */
public class DisplayNumbers {
    public static void main(String[] args) {
        // Loop from 0 to 1000, incrementing by 10
        for (int i = 0; i <= 1000; i += 10) {
            System.out.print(i + " ");
        }
    }
}

/**
 * Explanation:
The loop starts at 0 and runs until i reaches 1000.
In each iteration, the value of i is incremented by 10.
The result is printed on the same line with a space 
between the numbers.
 */
